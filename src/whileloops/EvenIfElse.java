package whileloops;

public class EvenIfElse {
	public static void main(String[] args) {
//		Print even numbers from 1 to 50 with if else condition.
		System.out.println("below list is even numbers from 1 to 50 :");
		int i = 1;
		while(i<=50) {
			
			if(i%2==0) {
				System.out.println(i);
			}
		i++;	
	}

}}
