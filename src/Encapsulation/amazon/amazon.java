package Encapsulation.amazon;

public class amazon {

		private String id;
		private String password;
		
		public void setId(String id) {
			this.id = id;
			
		}
		public String getId() {
			return id;
		}
		public void setPassword(String password) {
			this.password = password;
			
			
		}
		public String getPassword() {
			return password;
		}
}
